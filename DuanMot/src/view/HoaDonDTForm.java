    public void loadTable() {
        defaultTableModel = new DefaultTableModel();
        defaultTableModel = (DefaultTableModel) tbHDDT.getModel();
        defaultTableModel.setRowCount(0);
        for (HoaDonDoiTra hddt : this.ser.getAll()) {
            defaultTableModel.addRow(new Object[]{
                hddt.getId(), hddt.getMa(), hddt.getNgayDoi(), hddt.getNgayNhan(), hddt.getSoLuong()

            });
        }

    }

    public HoaDonDoiTra getFormData() {
        String id = lblId.getText().trim();
        String madt = txtMaDoiTRa.getText().trim();
        String ngaydt = txtNgayDT.getText().trim();
        String ngaynhan = txtNgayNhan.getText().trim();
        String sl = txtSoLuong.getText().trim();
        if (madt.length() == 0
                || ngaydt.length() == 0
                || ngaynhan.length() == 0
                || sl.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống");
            return null;
        }
        Date ngayDT = null;
        try {
            ngayDT = Date.valueOf(ngaydt);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập lại ngày");
            return null;
        }
        Date ngayn = null;
        try {
            ngayn = Date.valueOf(ngaynhan);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập lại ngày");
            return null;
        }
        int soluong = -1;
        try {
            soluong = Integer.parseInt(sl);
            if (soluong <= -1) {
                JOptionPane.showMessageDialog(this, "Nhập lại số lượng");
                return null;
            }
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Nhập số lượng");
            return null;
        }
        HoaDonDoiTra hddt = new HoaDonDoiTra();
        hddt.setMa(madt);
        hddt.setNgayDoi(ngayDT);
        hddt.setNgayNhan(ngayn);
        hddt.setSoLuong(soluong);
        return hddt;
    }

