    private void loadTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblCPU.getModel();

        dtm.setRowCount(0);

        for (CPU cpu : this.CPUSer.getAll()) {
            Object[] rowData = {
                cpu.getId(),
                cpu.getMa(),
                cpu.getTen(),};
            dtm.addRow(rowData);
        }
    }

    public CPU getFormData() {
        String id = lblId.getText().trim();
        String ma = txtMa.getText().trim();
        String loai = txtTen.getText().trim();
        if (ma.length() == 0
                || loai.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống");
            return null;
        }

        CPU cpu = new CPU(id, ma, loai);
        return cpu;
    }

