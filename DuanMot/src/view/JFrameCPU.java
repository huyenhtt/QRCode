    private void tblCPUMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCPUMouseClicked
        // TODO add your handling code here:
        int row = this.tblCPU.getSelectedRow();

        if (row == -1) {
            return;
        }
        String idStr = this.tblCPU.getValueAt(row, 0).toString();
        String ma = this.tblCPU.getValueAt(row, 1).toString();
        String dungLuong = this.tblCPU.getValueAt(row, 2).toString();
        this.lblId.setText(idStr);
        this.txtMa.setText(ma);
        this.txtTen.setText(dungLuong);
    }//GEN-LAST:event_tblCPUMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String Ma = txtSearch.getText();
        int i;

        for (i = 0; i < CPUSer.getAll().size(); i++) {
            CPU cpu = CPUSer.getAll().get(i);
            if (cpu.getMa().equalsIgnoreCase(Ma)) {
                JOptionPane.showMessageDialog(this, "Đã tìm thấy ");
                break;
            }
        }
        if (i == CPUSer.getAll().size()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        CPU cpu = this.getFormData();
        if (cpu == null) {
            return;
        }

        this.CPUSer.insert(cpu);
        this.loadTable();
        this.clearForm();
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        int row = this.tblCPU.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần sửa");
            return;
        }

        CPU cpu = this.getFormData();
        if (cpu == null) {
            return;
        }

        this.CPUSer.update(cpu, cpu.getMa());
        this.loadTable();
        this.clearForm();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = this.tblCPU.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần xóa");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                "Xác nhận xóa?");
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        String idStr = this.tblCPU.getValueAt(row, 0).toString();
        this.CPUSer.delete(idStr);
        this.loadTable();
        this.clearForm();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void clearForm() {
        this.lblId.setText("_");
        this.txtMa.setText("");
        this.txtTen.setText("");
    }

